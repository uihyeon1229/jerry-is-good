# =====================================================================
# Pydantic 메타데이터 (LLMStructuredColumn 용)
# =====================================================================


class TaxMetadata(BaseModel):
    """세법 CoT 메타데이터."""

    applied_law_mst: list[str] = Field(
        description="적용된 법령의 조문 식별자 (예: ['소득세법 제20조', '소득세법 시행령 제38조'])",
    )
    final_answer: str = Field(description="최종 결론 (수치 또는 명제)")
    num_reasoning_steps: int = Field(
        description="CoT 추론 단계 수", ge=1, le=20
    )
    concepts_used: list[str] = Field(
        description="사용된 세법 개념 (예: ['근로소득공제', '종합소득과세표준'])"
    )
    requires_calculation: bool = Field(
        description="실제 계산이 필요한 문제인지"
    )
    references_precedent: bool = Field(description="판례/해석례를 참조했는지")


